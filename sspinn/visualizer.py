class visualizer:

    def __init__(self,full,labels):
        self.full_mat = full
        self.labels = labels

    #def truncate(self):

    def draw2Dstructure(self):
    
        # Get labels
        label_dict = {}
        for i in range(len(self.full_mat)):
            label_dict[i] = labels[i]
    
        # Atom color keys
        atomcolor = {'C':'grey', 'O':'r', 'N':'b', 'H':'w', 'S':'orange', 'F':'y', 'Cl':'g', \
                     'Br':'purple', 'P':'c', 'I':'brown', 'B':'pink'}
        
        # Generate graph
        G = nx.from_numpy_matrix(self.full_mat)
        pos = graphviz_layout(G)
        
        # Get list of node colors
        color_list = []
        for i in range(len(labels)):
            color_list.append(atomcolor[labels[i]])
        
        # Draw Nodes
        nx.draw_networkx_nodes(G,pos,node_size=450,node_color=color_list)
    
        # Label bond order as weights on the self.full_mat
        triple = [(a,b) for (a,b,d) in G.edges(data=True) if d['weight'] >2.0 and d['weight'] <=3.0]
        double = [(a,b) for (a,b,d) in G.edges(data=True) if d['weight'] >1.0 and d['weight'] <=2.0]
        single = [(a,b) for (a,b,d) in G.edges(data=True) if d['weight'] <=1.0]
        
        # Draw Atom labels
        nx.draw_networkx_labels(G,pos,label_dict,font_size=12)
        
    
        # Draw triple bonds
        nx.draw_networkx_edges(G,pos,edgelist=triple,width=14)
        nx.draw_networkx_edges(G,pos,edgelist=triple,width=8,edge_color='w')
        nx.draw_networkx_edges(G,pos,edgelist=triple,width=3)
        
        # Draw double bonds
        nx.draw_networkx_edges(G,pos,edgelist=double,width=10)
        nx.draw_networkx_edges(G,pos,edgelist=double,width=4,edge_color='w')
        
        # Draw single bonds
        nx.draw_networkx_edges(G,pos,edgelist=single,width=3)
        
        plt.axis('off')
        plt.show()

